
package ActividadesRepasoTema1;

public class Ejercicio1_2 {
    public static void main(String[] args) {
        
        int a=1,b=2,c=3,s=4;
        String nombre = "Me llamo marcos";
        
        //En una sola linea
        System.out.println(a+" "+b+" "+" "+c+" "+" "+s+" "+nombre);
        System.out.println("+++++++++++++++++++++++++++++++++++++++");
        //En varias lineas con varias instrucciones println
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(s);
        System.out.println(nombre);
        
        //En varias lineas con un println
        System.out.println("++++++++++++++++++++");
        System.out.println(a+"\n"
                + b+"\n"
                + c+"\n"
                + s+"\n"
                + nombre+"\n");
    }
}
